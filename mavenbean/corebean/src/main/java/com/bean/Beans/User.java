package com.bean.Beans;

public class User {
    private String name;
    private String Address;
    public User() {
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAddress(String adress) {
        this.Address = adress;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return Address;
    }


}
